import java.util.*;

public class Enemy {
    // Health, move (damage, type), speed
    int curr_health;
    int speed;
    int attack;
    int defense;
    int exp;
    public Enemy(int h, int s, int a, int d, int e) {
        curr_health = h;
        speed = s;
        attack = a;
        defense = d;
        exp = e;

    }

}
